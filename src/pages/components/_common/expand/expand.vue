<template>
	<transition
		@before-enter="beforeEnter" 
		@enter="enter" 
		@after-enter="afterEnter" 
		@before-leave="beforeLeave" 
		@leave="leave" 
		@after-leave="afterLeave" 
	>
		<div v-show="isActive">
			<slot />
		</div>
	</transition>
</template>

<script>

export default {
	name: "c-expand",
	components: {
		expand: {
			type: Boolean,
			default: false
		}
	},
	data() {
		return {
			isActive: this.expand
		};
	},
	computed: {
		
	},
	methods: {
		beforeEnter(el) {
			el.className += ' c-collapse-transition';
			 if (!el.dataset) el.dataset = {};

			el.style.height = '0';
		},
		enter(el) {
			el.dataset.oldOverflow = el.style.overflow;
			if (el.scrollHeight !== 0) {
				el.style.height = el.scrollHeight + 'px';
			} else {
				el.style.height = '';
			}

			el.style.overflow = 'hidden';
		},
		afterEnter(el) {
			el.className = el.className.replace(' c-collapse-transition', '');
			el.style.height = '';
			el.style.overflow = el.dataset.oldOverflow;
		},
		beforeLeave(el) {
			if (!el.dataset) el.dataset = {};
			el.dataset.oldOverflow = el.style.overflow;

			el.style.height = el.scrollHeight + 'px';
			el.style.overflow = 'hidden';
		},
		leave(el) {
			if (el.scrollHeight !== 0) {
				el.className += ' c-collapse-transition';
				el.style.height = 0;
			}
		},
		afterLeave(el) {
			el.className = el.className.replace(' c-collapse-transition', '');
			el.style.height = '';
			el.style.overflow = el.dataset.oldOverflow;
		},

		handleToggle() {
			this.isActive = !this.isActive;
			this.$emit('change', this.isActive);
		}
	}
};
</script>
<style>
.c-collapse-transition {
	transition: height .2s ease-in-out
}
</style>